package com.desha.HotelBooker.domain.enums;

public enum RoomType {
    SINGLE,          // One bed, for one person
    DOUBLE,          // One double bed, for two people
    TWIN,            // Two single beds
    TRIPLE,          // Three single beds or one double + one single
    QUAD,            // Four-person room
    QUEEN,           // Queen-sized bed
    KING,            // King-sized bed
    STUDIO,          // Combined living/sleeping area with kitchenette
    SUITE,           // Bedroom + living area, more luxurious
    DELUXE,          // Higher quality than standard room
    FAMILY,          // Large room or connected rooms for families
    EXECUTIVE,       // For business travelers, may include workspace
    PENTHOUSE,       // Luxury top-floor suite
    VILLA,           // Independent unit, often with kitchen and living space
    CABANA,          // Poolside room with outdoor access
    BUNGALOW,        // Standalone small house-style room
    PRESIDENTIAL     // Top-tier luxury suite
}
